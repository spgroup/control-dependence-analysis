package samples;
public class WhileIfIfElseDoWhile {
    public static void main() {
        int x = 1;
        do{
            x = 2;
        }while (x < 1);
        x = 3;
        if (x<10){
            x = 4;
        }else{
            x = 5;
        }
        x = 6;
        while (x != 1) {
            x = 7;
        }
        x = 8;
        if (x != 23){
            x = 9;
        }
        x = 10;
    }
}