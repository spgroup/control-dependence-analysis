package samples;
public class TwoIfElse {
    public static void main() {
        int x = 1;
        if (x > 1) {
            x = 2;
        }else{
            x = 3;
        }
        x = 4;
        if (x < 10) {
            x = 5;
        }else{
            x = 6;
        }
        x = 7;
    }

}